# Arreglo unidimensional para almacenar los precios de los productos
precios = [15.50, 23.99, 9.99, 35.75, 12.50]

# Calcular el costo total del inventario
costo_total = sum(precios)

# Mostrar el costo total formateado a dos decimales
print(f"El costo total del inventario es: ${costo_total:.2f}")

